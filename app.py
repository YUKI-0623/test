import streamlit as st
import pandas as pd

st.set_page_config(page_title="UmAI 競馬予想アプリ 2026", layout="wide")
st.title("🏇 UmAI 競馬シミュレーター 2026")

# 1. 全18頭のデータを正確に網羅
def get_race_data():
    return pd.DataFrame({
        '馬名': ['ダノンデサイル', 'ミュージアムマイル', 'コスモキュランダ', 'ジューンテイク', 'クロワデュノール', 'ビザンチンドリーム', 'レガレイラ', 'スティンガーグラス', 'シュガークン', 'ミクニインスパイア', 'シンエンペラー', 'マイユニバース', 'ミステリーウェイ', 'ファミリータイム', 'シェイクユアハート', 'メイショウタバル', 'マイネルエンペラー', 'タガノデュード'],
        '馬場適性': [0.9, 0.5, 0.8, 0.6, 0.7, 0.85, 0.6, 0.7, 0.9, 0.5, 0.8, 0.6, 0.7, 0.75, 0.6, 0.95, 0.9, 0.6],
        '血統スタミナ': [0.8, 0.7, 0.9, 0.85, 0.7, 0.8, 0.95, 0.8, 0.7, 0.6, 0.9, 0.7, 0.8, 0.85, 0.75, 0.9, 0.95, 0.7],
        '騎手補正': [0.9, 0.7, 0.8, 0.75, 0.9, 0.8, 0.95, 0.7, 0.85, 0.6, 0.9, 0.65, 0.7, 0.75, 0.7, 0.95, 0.8, 0.65]
    })

# 2. UIと計算ロジック
df = get_race_data()

st.subheader("🛠 予想の微調整")
w1 = st.slider("馬場適性の重要度", 0.0, 1.0, 0.5)
w2 = st.slider("血統スタミナの重要度", 0.0, 1.0, 0.5)
base_time = st.slider("ベースタイム(秒)", 120.0, 140.0, 130.0)

if st.button("🚀 全18頭で予想を実行"):
    # 調整を加えた計算式（全頭に適用されます）
    df['秒'] = base_time - (df['馬場適性'] * w1 * 10) - (df['血統スタミナ'] * w2 * 10) - (df['騎手補正'] * 5)
    
    # 並び替え
    result = df.sort_values(by='秒').reset_index(drop=True)
    result['着順'] = result.index + 1
    result['予想タイム'] = result['秒'].apply(lambda x: f"{int(x//60)}:{x%60:.3f}")
    
    # グラフ表示（全頭表示）
    st.bar_chart(result.set_index('馬名')['秒'])
    
    # 表表示（全頭表示）
    st.table(result[['着順', '馬名', '予想タイム']])
    st.balloons()
