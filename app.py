import streamlit as st
import pandas as pd

st.set_page_config(page_title="泥んこ血統AI・最強カスタマイズ", layout="wide")
st.title("🏇 泥んこ血統AI & 自由カスタマイズ")

# ==========================================
# 1. データベース設定
# ==========================================
def get_base_data():
    return pd.DataFrame({
        '馬名': ['ダノンデサイル', 'ミュージアムマイル', 'コスモキュランダ', 'ジューンテイク', 'クロワデュノール', 'ビザンチンドリーム', 'レガレイラ', 'スティンガーグラス', 'シュガークン', 'ミクニインスパイア', 'シンエンペラー', 'マイユニバース', 'ミステリーウェイ', 'ファミリータイム', 'シェイクユアハート', 'メイショウタバル', 'マイネルエンペラー', 'タガノデュード'],
        '泥適性': [0.9, 0.5, 0.8, 0.6, 0.7, 0.85, 0.6, 0.7, 0.9, 0.5, 0.8, 0.6, 0.7, 0.75, 0.6, 0.95, 0.9, 0.6],
        '血統スタミナ': [0.8, 0.7, 0.9, 0.85, 0.7, 0.8, 0.95, 0.8, 0.7, 0.6, 0.9, 0.7, 0.8, 0.85, 0.75, 0.9, 0.95, 0.7],
        '騎手補正': [0.9, 0.7, 0.8, 0.75, 0.9, 0.8, 0.95, 0.7, 0.85, 0.6, 0.9, 0.65, 0.7, 0.75, 0.7, 0.95, 0.8, 0.65]
    })

# ==========================================
# 2. スライダー調整（サイドバー）
# ==========================================
st.sidebar.header("🛠 予想の微調整")
mud_factor = st.sidebar.slider("馬場の重さ（泥の影響度）", 0.0, 5.0, 2.0)
stamina_factor = st.sidebar.slider("血統スタミナの影響度", 0.0, 5.0, 2.0)
base_time = st.sidebar.slider("ベースタイム(秒)", 130.0, 150.0, 138.0)

# ==========================================
# 3. 計算ロジック
# ==========================================
df = get_base_data()

# 調整を加えた計算式
df['予測秒'] = base_time - (df['泥適性'] * mud_factor) - (df['血統スタミナ'] * stamina_factor) - (df['騎手補正'] * 1.5)

# 結果の整理
result = df.sort_values(by='予測秒').reset_index(drop=True)
result['着順'] = result.index + 1
result['予想タイム'] = result['予測秒'].apply(lambda x: f"{int(x//60)}:{x%60:.2f}")

# ==========================================
# 4. 表示
# ==========================================
st.subheader("📊 カスタマイズ結果：全18頭")

# グラフとテーブルを並べて表示
col1, col2 = st.columns([1, 2])
with col1:
    # グラフ：上位馬ほどバーが長くなるように調整
    result['能力スコア'] = result['予測秒'].max() - result['予測秒']
    st.bar_chart(result.set_index('馬名')['能力スコア'])

with col2:
    st.table(result[['着順', '馬名', '予想タイム']])
