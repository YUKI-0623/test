import streamlit as st
import pandas as pd

# ==========================================
# 0. スマホ向けの画面設定
# ==========================================
st.set_page_config(page_title="泥んこ競馬AI", page_icon="🏇", layout="centered")
st.title("🏇 泥んこ血統AI & 微調整シミュレーター")

# ==========================================
# 1. データの定義
# ==========================================
def get_base_prediction():
    data = {
        '馬名': ['ダノンデサイル', 'ミュージアムマイル', 'コスモキュランダ', 'ジューンテイク', 'クロワデュノール', 'ビザンチンドリーム', 'レガレイラ', 'スティンガーグラス', 'シュガークン', 'ミクニインスパイア', 'シンエンペラー', 'マイユニバース', 'ミステリーウェイ', 'ファミリータイム', 'シェイクユアハート', 'メイショウタバル', 'マイネルエンペラー', 'タガノデュード'],
        'ペナルティ': [0.8, 1.3, 0.8, 1.6, 1.0, 0.8, 2.3, 1.6, 1.3, 0.8, 0.5, 1.1, 2.3, 1.6, 2.3, 0.2, 0.2, 1.2]
    }
    return pd.DataFrame(data)

# ==========================================
# 2. UIデザインと再計算ロジック
# ==========================================
df = get_base_prediction()

st.subheader("🛠 予想の微調整")
weight = st.slider("馬場適性の重要度", 0.1, 2.0, 1.0, step=0.1)
bias = st.slider("タイム補正（全体）", -5.0, 5.0, 0.0)

if st.button("🚀 予想を実行"):
    # 調整を加えた予測タイムを計算
    df['予測タイム'] = (130.0 + df['ペナルティ']) * weight + bias
    
    # 並び替え（タイムが小さい＝速い順）
    result = df.sort_values(by='予測タイム').reset_index(drop=True)
    result['着順'] = result.index + 1
    
    st.success("🎯 計算完了！全頭表示します")
    
    # グラフ用データ：タイムが小さいほどバーを長くするために反転させた数値を生成
    chart_data = result.copy()
    chart_data['グラフ用スコア'] = chart_data['予測タイム'].max() - chart_data['予測タイム']
    
    # グラフ表示（馬名をインデックスにセットして表示）
    st.bar_chart(chart_data.set_index('馬名')['グラフ用スコア'])
    
    # 表表示
    st.table(result[['着順', '馬名', '予測タイム']])
    st.balloons()
