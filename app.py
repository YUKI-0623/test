import streamlit as st
import pandas as pd

# 画面全体の基本設定
st.set_page_config(page_title="泥んこ血統AI・開発ステップ3", layout="wide")
st.title("🏇 泥んこ血統AI × 総合データシミュレーター（Step 3）")
st.caption("【ステップ3】想定騎手実績 ＆ 道悪手腕データの完全統合")
st.markdown("---")

# ==========================================
# 1. 過去5年の宝塚記念ラップデータ（参考用定数）
# ==========================================
lap_summary = {
    'ハイペース（2022年型:タイトルホルダー）': {'前半3F': 33.9, '後半3F': 36.3, '展開特徴': 'タフな持久力勝負。血統スタミナが極めて重要。'},
    'ミドルペース（2023年型:イクイノックス）': {'前半3F': 34.6, '後半3F': 35.5, '展開特徴': '実力通りに決まる平均決着。総合力が問われる。'},
    'スローペース（2021年型:クロノジェネシス）': {'前半3F': 35.2, '後半3F': 34.4, '展開特徴': '直線のスピード瞬発力勝負。騎手の手腕やキレが重視。'},
    '道悪タフペース（2024年型:ブローザホーン）': {'前半3F': 34.5, '後半3F': 36.6, '展開特徴': '重馬場の消耗戦。泥適性とスタミナがないと全滅。'}
}

# ==========================================
# 2. 出走馬18頭の総合データベース（騎手データと実績を完全統合）
# ==========================================
def get_base_data():
    return pd.DataFrame({
        '馬名': ['ダノンデサイル', 'ミュージアムマイル', 'コスモキュランダ', 'ジューンテイク', 'クロワデュノール', 'ビザンチンドリーム', 'レガレイラ', 'スティンガーグラス', 'シュガークン', 'ミクニインスパイア', 'シンエンペラー', 'マイユニバース', 'ミステリーウェイ', 'ファミリータイム', 'シェイクユアハート', 'メイショウタバル', 'マイネルエンペラー', 'タガノデュード'],
        '系統': ['ロベルト系', 'キングカメハメハ系', 'ディープ系（タフ型）', 'ディープ系', 'ブラックタイド系', 'ロベルト系', 'ハーツクライ系', 'ディープ系', 'キングカメハメハ系', 'ロベルト系', '欧州系', 'ダート・パワー系', 'ハーツクライ系', 'ディープ系', 'ハーツクライ系', 'ステイゴールド系', 'ステイゴールド系', 'スプリント・パワー系'],
        '泥適性': [0.9, 0.5, 0.8, 0.6, 0.7, 0.85, 0.6, 0.7, 0.9, 0.5, 0.8, 0.6, 0.7, 0.75, 0.6, 0.95, 0.9, 0.6],
        'スタミナ': [0.8, 0.7, 0.9, 0.85, 0.7, 0.8, 0.95, 0.8, 0.7, 0.6, 0.9, 0.7, 0.8, 0.85, 0.75, 0.9, 0.95, 0.7],
        '同コース適性': [0.95, 0.40, 0.80, 0.75, 0.90, 0.60, 0.85, 0.70, 0.85, 0.50, 0.90, 0.30, 0.65, 0.60, 0.70, 0.85, 0.80, 0.65],
        '同距離適性': [0.90, 0.50, 0.85, 0.70, 0.85, 0.70, 0.80, 0.75, 0.90, 0.45, 0.85, 0.40, 0.70, 0.65, 0.75, 0.90, 0.85, 0.60],
        # 【新規】想定騎手名
        '騎手': ['横山典弘', '中井裕二', 'M.デムーロ', '藤岡佑介', '北村友一', 'ムルザバエフ', 'C.ルメール', '戸崎圭太', '武豊', '今村聖奈', '坂井瑠星', '池添謙一', '和田竜二', '松山弘平', '幸英明', '浜中俊', '川田将雅', '古川吉洋'],
        # 【新規】騎手のコース実績 ＆ 道悪での技術スコア
        '騎手実績スコア': [0.90, 0.50, 0.80, 0.75, 0.80, 0.85, 0.98, 0.85, 0.95, 0.40, 0.90, 0.85, 0.75, 0.85, 0.70, 0.80, 0.95, 0.60]
    })

# ==========================================
# 3. ユーザー操作パネル（UI）
# ==========================================
st.sidebar.header("🛠 1. 展開と馬場の設定")
selected_pace = st.sidebar.selectbox("想定するレース展開（過去ラップ参考）", list(lap_summary.keys()))
pace_info = lap_summary[selected_pace]
st.sidebar.info(f"💡 {pace_info['展開特徴']}")

mud_val = st.sidebar.slider("馬場の重さ（泥の影響度）", 0.0, 10.0, 5.0)
base_val = st.sidebar.slider("ベースタイム（秒）", 130.0, 150.0, 138.0)

st.sidebar.header("📈 2. 各種データの影響度調整")
course_weight = st.sidebar.slider("競馬場適性の重要度", 0.0, 5.0, 2.0)
distance_weight = st.sidebar.slider("距離適性(2200m)の重要度", 0.0, 5.0, 2.0)
# 【新規】騎手データの影響度を調整するスライダー
jockey_weight = st.sidebar.slider("騎手手腕・実績の重要度", 0.0, 5.0, 2.0)

# ==========================================
# 4. 展開に応じた「自動重み付け」ロジック
# ==========================================
if "ハイペース" in selected_pace:
    pace_stamina_weight = 3.5
    pace_jockey_weight = 1.0  # 展開による基礎騎手補正（ベース）
elif "スローペース" in selected_pace:
    pace_stamina_weight = 1.0
    pace_jockey_weight = 2.5  # スロー展開は騎手の位置取りの重要性が増す
elif "道悪" in selected_pace:
    pace_stamina_weight = 4.0
    pace_jockey_weight = 1.5
else:
    pace_stamina_weight = 2.0
    pace_jockey_weight = 1.5

# ==========================================
# 5. 計算と出力
# ==========================================
df = get_base_data()

# すべての要素（血統・コース・距離・ラップ・騎手）を統合した最終計算式
df['予測秒'] = (
    base_val 
    + (mud_val * (1.1 - df['泥適性'])) 
    - (df['スタミナ'] * pace_stamina_weight) 
    - (df['同コース適性'] * course_weight)
    - (df['同距離適性'] * distance_weight)
    # 選択された「展開による騎手重要度」と、ユーザーの「騎手重要度スライダー」を掛け合わせて補正
    - (df['騎手実績スコア'] * (pace_jockey_weight + jockey_weight))
)

# ランキングの確定
result = df.sort_values(by='予測秒').reset_index(drop=True)
result['着順'] = result.index + 1
result['予想タイム'] = result['予測秒'].apply(lambda x: f"{int(x//60)}:{x%60:.2f}")

# 画面への表示
col1, col2 = st.columns([1, 2])

with col1:
    st.subheader("📋 選択された展開のラップ")
    st.metric(label="参考 前半3F", value=f"{pace_info['前半3F']} 秒")
    st.metric(label="参考 後半3F", value=f"{pace_info['後半3F']} 秒")
    
    # タイム差の可視化
    result['能力スコア'] = result['予測秒'].max() - result['予測秒']
    st.bar_chart(result.set_index('馬名')['能力スコア'])

with col2:
    st.subheader("📊 4大データ統合型・AI予想ランキング")
    # すべての根拠データを網羅したテーブル
    st.table(result[['着順', '馬名', '系統', '騎手', '予想タイム', '同コース適性', '同距離適性', '騎手実績スコア']])
